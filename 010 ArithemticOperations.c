#include <stdio.h>

void main() {
    // Addition
    int add = 5 + 3;
    printf("Addition: %d\n", add);

    // Subtraction
    int subtract = 7 - 4;
    printf("Subtraction: %d\n", subtract);

    // Multiplication
    int multiply = 6 * 9;
    printf("Multiplication: %d\n", multiply);

    // Division
    float divide = 10.0 / 3.0;
    printf("Division: %.2f\n", divide);

    // Modulus
    int modulus = 11 % 4;
    printf("Modulus: %d\n", modulus);
}
